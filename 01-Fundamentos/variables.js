/*
Variables

Las variables son contenedores que almacenan datos.

Para usar vriables, se declaran usando las palabras reservadas:

var ->Es la forma tradicional. Pero ya no se recomienda.
let ->Es la forma moderna y la recomendada
const -> EL dato almacenado no puede cambiar.

Para declrar una variable:

var (nombre de la variable)= valor;

*/ 

var nombre ="Mike";
let edad= 21;
const vivo= true;

console.log(nombre);
console.log(edad);
console.log(vivo);

